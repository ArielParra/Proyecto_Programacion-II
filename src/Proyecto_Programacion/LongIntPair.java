package Proyecto_Programacion;

public class LongIntPair {
    private long first;
    private int second;

    public LongIntPair(long first, int second) {
        this.first = first;
        this.second = second;
    }

    public long getFirst() {
        return first;
    }

    public void setFirst(long first) {
        this.first = first;
    }

    public int getSecond() {
        return second;
    }

    public void setSecond(int second) {
        this.second = second;
    }
}
